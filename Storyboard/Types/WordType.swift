import Foundation

enum WordType: String, Codable {
		case noun = "noun"
		case pronoun = "pronoun"
		case verb = "verb"
		case adjective = "adjective"
		case adverb = "adverb"
		case preposition = "preposition"
		case conjunction = "conjunction"
		case determiner = "determiner"
		case modalVerb = "modal verb"
}
